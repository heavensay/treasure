public class Bean {
    private int age;
    private String name;
    private boolean isLady;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isLady() {
        return isLady;
    }

    public void setLady(boolean lady) {
        isLady = lady;
    }
}
